    public void showUserRecord(ClientProfessionalBean objBean) {

        txtClientID.setText(String.valueOf(objBean.getClient_ID()));
        txtProfession.setText(objBean.getProfession());
        txtOfficeAddress.setText(objBean.getOffice_Address());

        txtOfficePhone.setText(objBean.getOffice_Phone());
        txtDesignation.setText(objBean.getDesignation());
        txtAnnualIncome.setText(String.valueOf(objBean.getAnnual_Income()));

        txtOtherIncome.setText(String.valueOf(objBean.getIncome_Other()));

    }

