    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        String result = "";
        int flag = 0;
        ClientProfessionalBean objBean = new ClientProfessionalBean();



        if (txtClientID.getText().equals("")) {

            jOptionPane1.showMessageDialog(this, "User ID is missing", "ERROR", jOptionPane1.ERROR_MESSAGE);
        } else if (txtProfession.getText().equals("")) {
            jOptionPane1.showMessageDialog(this, "Name is missing", "ERROR", jOptionPane1.ERROR_MESSAGE);
        } else if (txtOfficeAddress.getText().equals("") && txtAnnualIncome.getText().equals("")) {
            jOptionPane1.showMessageDialog(this, "One out of Phone and Mobile is required", "ERROR", jOptionPane1.ERROR_MESSAGE);
        } else {
            try {

                objBean.setClient_ID(Integer.parseInt(txtClientID.getText()));
                objBean.setProfession(txtProfession.getText());
                objBean.setOffice_Address(txtOfficeAddress.getText());

                objBean.setOffice_Phone(txtOfficePhone.getText());
                objBean.setDesignation(txtDesignation.getText());
                objBean.setIncome_Other(Double.parseDouble(txtOtherIncome.getText()));

                objBean.setAnnual_Income(Double.parseDouble(txtAnnualIncome.getText()));
                result = objDB.updateClientProfessionalDetail(objBean);
            } catch (Exception e) {
                System.out.println("Exception in btnSaveActionPerformed () : " + e);
            }

        }

        if (result.equals("updated")) {
            flag = 1;
            alstClientProfessional = objDB.getAllClientProfessionalDetailList();
            ClientProfessionalBean objBean1 = (ClientProfessionalBean) alstClientProfessional.get(count);
            showUserRecord(objBean1);
            jOptionPane1.showMessageDialog(null, "Record has been successfully updated", "Record Updated", 1);
        } else if (result.equals("failed")) {
            jOptionPane1.showMessageDialog(null, "Record not updated", "ERROR", 2);
        }
        if (flag == 1) {
            disable(false);
            btnSave.setEnabled(false);
            btnAdd.setEnabled(true);
            btnUpdate.setEnabled(true);

            btnFirst.setEnabled(true);
            btnPrevious.setEnabled(true);
            btnNext.setEnabled(true);
            btnLast.setEnabled(true);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

